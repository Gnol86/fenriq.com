/**
 * ESLint Custom Rule: no-edit-boilerplate
 *
 * This rule prevents editing boilerplate files when working in a derived project.
 * It checks if:
 * 1. The current repo has an 'upstream' remote (indicating it's a project)
 * 2. The file being edited is a boilerplate file (not in src/project/)
 *
 * If both conditions are true, it throws an error.
 */

const { execSync } = require("child_process");
const path = require("path");

// Boilerplate paths that should not be edited in projects
const BOILERPLATE_PATTERNS = [
    /^src\/actions\/.*\.action\.js$/,
    /^src\/components\/(?!project\/).*$/,
    /^src\/lib\/(?!project\/).*$/,
    /^src\/hooks\/(?!project\/).*$/,
    /^src\/features\/(?!project\/).*$/,
    /^src\/app\/\(pages\)\/.*$/,
    /^src\/app\/\(auth\)\/.*$/,
    /^src\/app\/layout\.js$/,
    /^prisma\/schema\/base\.prisma$/,
    /^src\/messages\/(fr|en|nl|de)\.json$/,
];

// Check if current repo is a project (has upstream remote)
function isProject() {
    try {
        const remotes = execSync("git remote", { encoding: "utf-8" });
        return remotes.includes("upstream");
    } catch (_e) {
        // Not a git repo or error - assume not a project
        return false;
    }
}

// Check if file matches boilerplate patterns
function isBoilerplateFile(filePath) {
    // Get relative path from project root
    const relativePath = path.relative(process.cwd(), filePath);

    return BOILERPLATE_PATTERNS.some(pattern => pattern.test(relativePath));
}

module.exports = {
    meta: {
        type: "problem",
        docs: {
            description:
                "Prevent editing boilerplate files in derived projects",
            category: "Possible Errors",
            recommended: true,
        },
        messages: {
            boilerplateEdit:
                "🚨 BOILERPLATE FILE - Do not edit this file in your project!\n" +
                "This file is managed by the boilerplate and will be overwritten on updates.\n" +
                "Put your project code in: src/project/\n" +
                "See: .github/SETUP_NEW_PROJECT.md",
        },
        schema: [],
    },

    create(context) {
        // Only run this rule if we're in a project (not the boilerplate itself)
        if (!isProject()) {
            return {};
        }

        const filePath = context.getFilename();

        // Check if this is a boilerplate file
        if (isBoilerplateFile(filePath)) {
            return {
                Program(node) {
                    context.report({
                        node,
                        messageId: "boilerplateEdit",
                    });
                },
            };
        }

        return {};
    },
};
